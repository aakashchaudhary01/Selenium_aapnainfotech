package ControlStatements;

public class DoWhileLoopExample01 {

	public static void main(String[] args) {
		
		//initialization
		 int i=11;    
		    do{    
		        System.out.println(i); 
		    
		    //update expression
		    i++; 
		    //checking the condition
		    }while(i<=10);    
	}

}
